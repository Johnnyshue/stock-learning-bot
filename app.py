@app.route('/api/stock/<symbol>')
def stock_lookup(symbol):
    """查詢即時股票資料"""
    # Sanitize symbol input
    safe_symbol = str(escape(symbol.strip()))[:20]
    try:
        import yfinance as yf
        ticker = yf.Ticker(safe_symbol)
        info = ticker.info

        if not info or ('currentPrice' not in info and 'regularMarketPrice' not in info):
            return jsonify({"error": f"找不到 {safe_symbol}，請確認代號（台股加 .TW，如 2330.TW）"})

        price = info.get('currentPrice') or info.get('regularMarketPrice', 0)
        prev = info.get('previousClose', price)
        change = round(price - prev, 2)
        change_pct = round(change / prev * 100, 2) if prev else 0

        def fmt_cap(v):
            if not v:
                return 'N/A'
            if v >= 1e12:
                return f'{v/1e12:.1f}T'
            if v >= 1e9:
                return f'{v/1e9:.0f}B'
            if v >= 1e6:
                return f'{v/1e6:.0f}M'
            return f'{v:,.0f}'

        return jsonify({
            "symbol": safe_symbol.upper(),
            "name": info.get('shortName', info.get('longName', safe_symbol)),
            "price": f'{price:,.2f}',
            "change": change,
            "change_pct": change_pct,
            "pe": f'{info["trailingPE"]:.1f}' if info.get('trailingPE') else 'N/A',
            "eps": f'{info["trailingEps"]:.2f}' if info.get('trailingEps') else 'N/A',
            "dividend_yield": f'{info["dividendYield"]*100:.2f}%' if info.get('dividendYield') else 'N/A',
            "market_cap": fmt_cap(info.get('marketCap')),
            "high_52w": f'{info["fiftyTwoWeekHigh"]:,.2f}' if info.get('fiftyTwoWeekHigh') else 'N/A',
            "low_52w": f'{info["fiftyTwoWeekLow"]:,.2f}' if info.get('fiftyTwoWeekLow') else 'N/A',
        })
    except Exception as e:
        return jsonify({"error": f"查詢失敗：{str(e)}"})


